package collection_list;

import java.util.TreeMap;

public class Tree_map {

	public static void main(String[] args) {
		
		// map[interface].....(child) Tree map
		
		// Tree map use key, value generic method
			
		// Tree_map  methods  same as hash&Linkedhash_map methods
			
		// only print ascending order or natural order
			
		// duplicate key not allow & duplicate value allow
			
		// null value or key not allow // use means null exception pass
			
		
    
		TreeMap<String, Integer> treemap = new TreeMap<String,Integer>();
		
		treemap.put("total strength", 20);
		treemap.put("total present", 17);
		treemap.put("total absent", 03);
		treemap.put("total total", 03);
		
		System.out.println("add:"+treemap);
		
	
		
	}          

}
